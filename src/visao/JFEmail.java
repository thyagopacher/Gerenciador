    private void bEnviaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEnviaActionPerformed
        this.setCursor(getPredefinedCursor(Cursor.WAIT_CURSOR));
        Thread tprocurar = new Thread(() -> {
            if(tDestino.getText() != null && tDestino.getText().isEmpty() && 
               tAssunto.getText() != null && !tAssunto.getText().isEmpty() && 
               tMensagem.getText() != null && !tMensagem.getText().isEmpty()){
                msg = email.sendMail(null, tDestino.getText(), tAssunto.getText(), tMensagem.getText());
                if(msg != null && !msg.isEmpty()){
                    showMessageDialog(null, msg, "ERRO na mensagem:", JOptionPane.ERROR_MESSAGE);
                }else{
                    showMessageDialog(null, "Mensagem enviada com sucesso");
                }
            }else{
                showMessageDialog(null, "Faltou preencher algum campo na mensagem, por favor verifique!", "ERRO na mensagem:", JOptionPane.ERROR_MESSAGE);
            }        
        });
        tprocurar.start();
        this.setCursor(getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_bEnviaActionPerformed

