    private void logar() {
        String msg = "";    
        if (tLogin.getText() != null && !tLogin.getText().isEmpty() && tSenha.getPassword() != null) {
            pessoa = prn.validaLogin(tLogin.getText(), new String(tSenha.getPassword()));
            if (pessoa != null && pessoa.getNome() != null && !pessoa.getNome().isEmpty()) {
                dispose();
                new JFPrincipal(cx, pessoa, internet).setVisible(true);
            } else {
                msg += "\nLogin ou senha inv√°lidos";
            }
        }else{
            msg += "\nLogin ou senha em branco";
        }
        if(!msg.isEmpty()){
            showMessageDialog(null, msg, "ERRO:", JOptionPane.ERROR_MESSAGE);
        }    
    }
