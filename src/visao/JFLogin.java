    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Thread tcomeco = new Thread(() -> {
            String msg = validacao.verificaInternet();
            if(msg != null && !msg.isEmpty()){
                internet = false;
                showMessageDialog(null, msg + "\nO programa funcionara de forma limitada", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }else{
                internet = true;
                lerro.setText("Conectado a internet");
            }
            cx.conectarBanco();
            if(msg == null || msg.isEmpty()){
                prn = new PessoaRN(cx);
            }else{
                showMessageDialog(null, msg, "Erro:", JOptionPane.ERROR_MESSAGE);  
            }
        });
        tcomeco.start();
    }//GEN-LAST:event_formWindowOpened

