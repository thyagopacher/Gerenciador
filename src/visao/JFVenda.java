    public void escutaPessoa(){
            bProcurarCliente.addActionListener(new java.awt.event.ActionListener()
            {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent e){
                    onAlterarDadosPessoa();
                    if(pessoa != null && pessoa.getNome() != null){
                        tPessoa.setText(pessoa.getNome().toUpperCase(getDefault()));
                    }
                }
            });                   
    }
    
