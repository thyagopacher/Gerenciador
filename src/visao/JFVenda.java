    private void bPagSeguroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPagSeguroActionPerformed
        if(obj_item != null && !obj_item.isEmpty()){
            pagseguro.adicionaLoja(null, null);
            pagseguro.adicionaEndereco(null, pessoa.getEstado().toUpperCase(getDefault()), null, pessoa.getCidade().toUpperCase(getDefault()), 
            pessoa.getBairro().toUpperCase(getDefault()), pessoa.getLogradouro().toUpperCase(), 
            pessoa.getCep(), valueOf(pessoa.getNum()), pessoa.getComplemento());
            pagseguro.adicionaItem(obj_item);
            pagseguro.adicionaCliente(pessoa.getNome(), pessoa.getEmail(), null, pessoa.getTelefone());
        }else{
            showMessageDialog(null, "Sem itens de venda", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bPagSeguroActionPerformed

