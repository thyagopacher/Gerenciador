    public void setaTela(){
        tPessoa.setText(compra.getPessoa().getNome().toUpperCase(getDefault()));
    }
    
    public void escutapessoa(){
            tPessoa.addActionListener(new java.awt.event.ActionListener()
            {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent e){
                    onAlterarDadospessoa();
                    if(pessoa != null && pessoa.getNome() != null){
                        setaTela();
                    }
                }
            });                   
    }
    
    /**chama formul치rio de busca**/
    protected void onAlterarDadospessoa(){
        JFConsultaPessoa  jf  = new JFConsultaPessoa(cx);
        if(jf.alterarDadosPessoa() == true){
            if(jf.pessoa != null){
                this.pessoa = (jf.pessoa);
            }
            jf.dispose();
        }
    }     

   /**lista tudo o que est치 na tabela**/  
    private void listar() {
            tabela.setModel(new TableModel(titulo, obj.size(), titulo.length));
            for(int i=0; i < obj.size(); i++){
                    tabela.setValueAt(obj.get(i).getCodigo(), i, 0);
                    tabela.setValueAt(obj.get(i).getPessoa().getNome().toUpperCase(getDefault()), i, 1);
                  //  tabela.setValueAt(obj.get(i).getProduto().getNome().toUpperCase(), i, 2);
            }
            tabela.setAutoCreateRowSorter(true);    
    } 

    private void setaObjeto() {
        compra.setDtcompra(new Date());
        compra.setDtvencimento(new Date());
        compra.setParcela(valueOf(tQuantidade.getText()));
        compra.setPessoa(pessoa);
    }
