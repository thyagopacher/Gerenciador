    private void bBuscarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarCompraActionPerformed
        this.setCursor(getPredefinedCursor(Cursor.WAIT_CURSOR));
        Thread tprocurar = new Thread(() -> {
            obj = crn.procuraLista("nome", tPessoa.getText(), "parcial");
            if(obj != null && !obj.isEmpty()){
                listar();
            }else{
                showMessageDialog(null, "Nada encontrado");
            }
        });
        tprocurar.start();
        this.setCursor(getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_bBuscarCompraActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Thread tinserir = new Thread(() -> {
            compra    = new Compra();
            setaObjeto();
            String msg = crn.inserirObjeto(compra);
            if(msg == null || msg.isEmpty()){
                msg = "Inserir efetuado com sucesso";
            }
            if(!msg.isEmpty()){
                ++qtd;
                obj.add(compra);
                listar();
                showMessageDialog(null, msg);
            }
        });
        tinserir.start();
    }//GEN-LAST:event_jButton1ActionPerformed

