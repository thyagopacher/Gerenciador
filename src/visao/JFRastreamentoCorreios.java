   /**lista tudo o que está na tabela**/  
    private void listar() {
        Thread tlistar = new Thread(() -> {
            int i = 0;
            tabela.setModel(new TableModel(titulo, colRegistoRastreamento.size(), titulo.length));
            for (RegistroRastreamento rr : colRegistoRastreamento) {             
                tabela.setValueAt(rr.getLocal(), i, 0);
                if(rr.getDataHora() != null){
                    tabela.setValueAt(rr.getDataHora(), i, 1);
                }else{
                    tabela.setValueAt("Sem data", i, 1);
                }
                if(rr.getDetalhe() != null && !rr.getDetalhe().isEmpty()){
                    tabela.setValueAt(rr.getDetalhe(), i, 2);  
                }else{
                    tabela.setValueAt("Sem detalhes", i, 2);  
                }
                if(rr.getAcao() != null && !rr.getAcao().isEmpty()){
                    tabela.setValueAt(rr.getAcao(), i, 3);  
                }else{
                    tabela.setValueAt("Sem ação", i, 3); 
                }
                i++;
            }
            tabela.setAutoCreateRowSorter(true);            
    });
    tlistar.start();
    } 
