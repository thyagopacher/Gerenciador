    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.setCursor(getPredefinedCursor(Cursor.WAIT_CURSOR));
        Thread tprocurar = new Thread(() -> {
            if(tCodigo.getText() != null && !tCodigo.getText().isEmpty()){
                try{
                    colRegistoRastreamento = rastrear(tCodigo.getText()); 
                    if(colRegistoRastreamento.size() > 0){
                        listar();
                    }else{
                        showMessageDialog(null, "Nada encontrado", "ERRO:", JOptionPane.INFORMATION_MESSAGE);
                    }
                }catch(HeadlessException ex){
                    showMessageDialog(null, ex, "ERRO:", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                showMessageDialog(null, "Não pode pesquisar em branco", "ERRO:", JOptionPane.INFORMATION_MESSAGE);
            }        
        });
        this.setCursor(getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        tprocurar.start();
    }//GEN-LAST:event_jButton2ActionPerformed

