    private void listar() {
            if(tabela.getRowCount() > 0){
                tm.limpar();
            }
            tm.addLista(obj);
    } 

    private void setaTela() {
        jTextField1.setText(servico.getNome());
    }
    
    private void setaObjeto(){
        servico.setNome(jTextField1.getText());
    }
