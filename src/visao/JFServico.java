    private void bProcurarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bProcurarServicoActionPerformed
        this.setCursor(getPredefinedCursor(Cursor.WAIT_CURSOR));
        servico = srn.procurarObjeto("nome", "");
        if(servico != null){
            setaTela();
        }
        this.setCursor(getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_bProcurarServicoActionPerformed

